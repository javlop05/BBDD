    public AllQuestionsEditor(ListModel<Pregunta> model, AllQuestionsController controller) {
        this.controller = controller;
        initComponents();
        this.setLocationRelativeTo(null);
        listaPreguntas = new JList<>();
        scrollPreguntas.setViewportView(listaPreguntas);
        listaPreguntas.setCellRenderer(new QuestionCellRenderer());
        listaPreguntas.setModel(model);
        final JDialog that = this;
        listaPreguntas.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2) {
                    int index = listaPreguntas.locationToIndex(e.getPoint());
                    controller.updateQuestion(model.getElementAt(index), that);
                    listaPreguntas.repaint();
                }
            }
            
        });
        controller.gatherAllQuestions();
    }

