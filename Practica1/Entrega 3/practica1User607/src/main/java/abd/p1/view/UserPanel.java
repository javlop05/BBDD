    public void rellenarInfo(String nombre, byte[] imagen, Date fechaNac) {
        labelNombre.setText(nombre);
        //Como la fecha no es un dato necesario puede ser que sea null
        if (fechaNac != null) {
            int edad = Usuario.calcularEdad(fechaNac);
            labelEdad.setText(edad + " aÃ±os");
        } else {
            labelEdad.setText("Sin especificar");
        }
        //Como la imagen no es un dato necesario puede ser que sea null
        if (imagen != null && imagen.length > 0) {
            setIcon(new ImageIcon(imagen));
        }
    }
    
