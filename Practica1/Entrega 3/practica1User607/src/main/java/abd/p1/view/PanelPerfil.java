    public void rellenarDatos(Usuario user) {
        areaDescripcion.setText(user.getDescripcion());
        labelSexo.setText(user.getGenero().toString());
        labelGeneroInteres.setText(user.getGeneroInteres().toString());
        //Rellenamos las aficiones
        for (String aficion: user.getAficiones()) {
            listModel.addElement(aficion);
        }
        userPanel.rellenarInfo(user.getNombre(), user.getImagen(), user.getFechaNac());
    }

