    private void cambiarAvatarActionPerformed(java.awt.event.ActionEvent evt) {
        ImageIcon avatar = controller.cambiarAvatar();
        if (avatar != null) {
            userPanel.setIcon(avatar);
        }
    }
    
    private void anadirAficionActionPerformed(java.awt.event.ActionEvent evt) {
        String aficion = controller.añadirAficion();
        if (aficion != null) {
           listModel.addElement(aficion);
        }
    }
     
    private void editarSeleccionadaActionPerformed(java.awt.event.ActionEvent evt) {
       String aficion = jListAficiones.getSelectedValue();
       if (aficion != null) {
           String nuevaAficion = controller.editarAfición(aficion);
           if (!nuevaAficion.equals(aficion)) {
               listModel.removeElement(aficion);
               listModel.addElement(nuevaAficion);
           }
       }
    }
    
    private void eliminarSeleccionadaActionPerformed(java.awt.event.ActionEvent evt) {
       String aficion = jListAficiones.getSelectedValue();
       controller.eliminarAficion(aficion);
       listModel.removeElement(aficion);
    }
      
    private void cambiarSexoActionPerformed(java.awt.event.ActionEvent evt) {
        Genero sexo = controller.cambiarSexo();
        labelSexo.setText(sexo.toString());
    } 
     
    private void cambiarPreferenciaActionPerformed(java.awt.event.ActionEvent evt) {
        Genero preferencia = controller.cambiarPreferencia();
        labelGeneroInteres.setText(preferencia.toString());
    }
    
    
